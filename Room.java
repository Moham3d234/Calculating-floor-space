public class Room	  	   	  	        	    		        	
{	  	   	  	        	    		        	
    private double width;	  	   	  	        	    		        	
    private double length;	  	   	  	        	    		        	
    private int level;	  	   	  	        	    		        	
	  	   	  	        	    		        	
    public void setWidth(double width)	  	   	  	        	    		        	
    {	  	   	  	        	    		        	
       this.width = width;	  	   	  	        	    		        	
    }	  	   	  	        	    		        	
	  	   	  	        	    		        	
    public void setLength(double length)	  	   	  	        	    		        	
    {	  	   	  	        	    		        	
       this.length = length;	  	   	  	        	    		        	
    }	  	   	  	        	    		        	
	  	   	  	        	    		        	
    public void setLevel(int level)	  	   	  	        	    		        	
    {	  	   	  	        	    		        	
       this.level = level;	  	   	  	        	    		        	
    }	  	   	  	        	    		        	
	  	   	  	        	    		        	
	  	   	  	        	    		        	
	  	   	  	        	    		        	
	  	   	  	        	    		        	
    public double getWidth()	  	   	  	        	    		        	
    {	  	   	  	        	    		        	
       return this.width;	  	   	  	        	    		        	
    }	  	   	  	        	    		        	
	  	   	  	        	    		        	
    public double getLength()	  	   	  	        	    		        	
    {	  	   	  	        	    		        	
       return this.length;	  	   	  	        	    		        	
    }	  	   	  	        	    		        	
	  	   	  	        	    		        	
    public int getLevel()	  	   	  	        	    		        	
    {	  	   	  	        	    		        	
       return this.level;	  	   	  	        	    		        	
    }	  	   	  	        	    		        	
	  	   	  	        	    		        	
	  	   	  	        	    		        	
	  	   	  	        	    		        	
}